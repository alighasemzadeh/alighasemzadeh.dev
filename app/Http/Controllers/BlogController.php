     */
    public function index()
    {
        $posts = WinkPost::with(['tags', 'author'])
            ->live()
            ->where('language', App::getLocale())
            ->orderBy('publish_date', 'DESC')
            ->paginate(9);
        return view('blog.index', [
            'posts' => $posts,
        ]);

